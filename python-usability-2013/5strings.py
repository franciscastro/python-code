# String samples

sample_sentence1 = "The 2012 Olympics \nwas held in London."

sample_sentence2 = "The 2012 \"Olympics\" was held in London."

sample_sentence3 = "The 2012 \'Olympics\' was held in London."

first_string = "Team"

second_string = "GB"

# String outputs

print ( sample_sentence1 )  # Escape: \n

print ( sample_sentence2 )  # Escape: \"

print ( sample_sentence3 )  # Escape: \'

print ( first_string + second_string )  # Concatenation

print ( first_string * 5 )    # Repeat

print ( first_string[ 0 ] )   # Subscript

print ( first_string[ 0 : 2 ] )   # Slice

print ( first_string[ 1 : 3 ] )   # Slice


